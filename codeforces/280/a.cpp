namespace std {
    template <>
    class hash<pair<long long,long long>> {
    public:
        size_t operator()(const pair<long long, long long>& p) const {
            return hash<long long>()(p.first) ^ (hash<long long>()(p.second) << 32);
        }
    };
};

int main() {
    std::ios_base::sync_with_stdio(false);

    int n;
    cin >> n;
    int count = 1;
    while (n > 0) {
        n -= (count * (count +1) / 2);
        ++count;
    }
    if (n < 0)
        --count;
    cout << count-1 << endl;


    return 0;
}

