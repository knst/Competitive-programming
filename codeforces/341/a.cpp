int gcd(int a, int b) {
    if (b == 0)
        return a;
    return gcd(b, a % b);
}

int main() {
    std::ios_base::sync_with_stdio(false);
    cin.tie(0);

    size_t n;
    cin >> n;
    vector<int64_t> values(n);
    int64_t sum = 0;
    int64_t minimal = 2e9;
    for (auto& i :values) {
        cin >> i;
        sum += i;
        if (i % 2 && i < minimal)
            minimal = i;
    }
    if (sum % 2) {
        if (minimal)
            sum -= minimal;
        else
            sum = 0;
    }
    cout << sum << endl;

    return 0;
}
