struct TheMatrix {
    int MaxArea(vector<string> board) {
        int n = board.size();
        int m = board[0].size();
        vector<vector<int>> v(n);
        for (int i = 0; i < n; ++i)
            v[i].resize(m);

        for (int x = 0; x < n; ++x) {
            v[x][0] = 1;
            for (int y = 1; y < m; ++y) {
                if (board[x][y] == board[x][y-1])
                    v[x][y] = 1;
                else
                    v[x][y] = v[x][y-1] + 1;
            }
        }
        int maximal = 0;
        for (int y = 0; y < m; ++y) {
            for (int len = 0; len < n; ++len) {
                for (int x = 0; x + len < n; ++x) {
                    int minimum = max(n,m);
                    for (int j = 1; j <= len; ++j)
                        if (board[x+j-1][y] == board[x+j][y])
                            minimum = 0;
                    for (int j = 0; j <= len; ++j) {
                        minimum = min(minimum, v[x + j][y]);
                    }
                    maximal = max((len + 1)* minimum, maximal);
                }
            }
        }
        return maximal;
    }

};


